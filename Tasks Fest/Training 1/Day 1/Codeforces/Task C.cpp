void Solve() {
    int n; cin >> n;

    set<int> ans;
    for (int i = 1; i * i <= n; i++) {
        int f = i;
        int s = n / f;

        ans.insert(f);
        ans.insert(s);
    }
    ans.insert(0);

    cout << ans.size() << '\n';
    for (auto p : ans) {
        cout << p << ' ';
    }
    cout << '\n';
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    int multitest = true;
    if (multitest == true) {
        int ctt; cin >> ctt;

        for (int i = 0; i < ctt; i++) {
            Solve();
        }
    }
    else {
        Solve();
    }

    return 0;
}
