#define int long long

void Solve() {
    int x, y; cin >> x >> y;

    int ans = 0;

    for (int k = 1; k * k <= x; k++) {
        int l = k + 1;
        int r = min(y, x / k - 1);

        if (l <= r) {
            ans += r - l + 1;
        }
    }

    cout << ans << '\n';
}

signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    cout.tie(0);

    int multitest = true;
    if (multitest == true) {
        int ctt; cin >> ctt;

        for (int i = 0; i < ctt; i++) {
            Solve();
        }
    }
    else {
        Solve();
    }

    return 0;
}
