int fast_power_mod(int a, int n, int mod) {
    if (n == 0) {
        return 1;
    }
    int result = 1;
    int base = a;
    while (n > 0) {
        if (n % 2 == 1) {
            result = (result * base) % mod;
        }
        base = (base * base) % mod;
        n = n / 2;
    }
    return result % mod;
}

