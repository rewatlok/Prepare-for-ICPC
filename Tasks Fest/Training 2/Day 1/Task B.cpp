void Solve() {
    int n; cin >> n;
    vector<int> ANS(n);

    vector<int> c(n);
    for (int i = 0; i < n; i++) {
        cin >> c[i];
        --c[i];
    }

    vector<vector<int>> g(n);
    for (int i = 0; i < n - 1; i++) {
        int u, v; cin >> u >> v;

        --u;
        --v;

        g[u].push_back(v);
        g[v].push_back(u);

        if (c[u] == c[v]) {
            ANS[c[u]] = 1;
        }
    }

    vector<int> C(n);

    for (int u = 0; u < n; u++) {
        C[c[u]] += 1;

        for (int v : g[u]) {
            C[c[v]] += 1;

            if (C[c[v]] == 2) {
                ANS[c[v]] = 1;
            }
        }

        C[c[u]] = 0;

        for (int v : g[u]) {
            C[c[v]] = 0;
        }
    }

    for (int i = 0; i < n; i++) {
        cout << ANS[i];
    } cout << '\n';
}

