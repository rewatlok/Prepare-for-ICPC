int F(int k, vector<int>& c, int n, int b, int x) {
    vector<int> groups(k);
    int ans = 0;
    for (int i = 0; i < n; i++) {
        int s = 0;
        for (int j = 0; j < c[i]; j++) {
            s += 1;
            groups[j % k] += 1;
            ans += (s - groups[j % k]);
        }
        for (int j = 0; j < c[i]; j++) {
            groups[j % k] = 0;
        }
    }
    return ans * b - (k - 1) * x;
}

void Solve() {
    int n, b, x; cin >> n >> b >> x;
    vector<int> c(n);
    for (int i = 0; i < n; i++) {
        cin >> c[i];
    }

    int S = 0;
    for(int i = 0; i < n; i++) {
        S += c[i];
    }

    int l = 1;
    int r = S;
    while (l + 3 <= r) {
        int m1 = l + (r - l) / 3;
        int m2 = r - (r - l) / 3;

        if (F(m1, c, n, b, x) >= F(m2, c, n, b, x)) {
            r = m2;
        }
        else {
            l = m1;
        }
    }

    int ans = 0;
    for (int i = l; i <= r; i++) {
        ans = max(ans, F(i, c, n, b, x));
    }

    cout << ans << '\n';
}

