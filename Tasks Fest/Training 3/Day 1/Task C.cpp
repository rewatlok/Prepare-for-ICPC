void Solve() {
    int a, b, k; cin >> a >> b >> k;

    int A = a; int B = b; int mxcnt = 0;
    for (int i = 2; i * i <= max(a, b); i++) {
        while (A % i == 0) {
            A /= i;
            mxcnt += 1;
        }

        while (B % i == 0) {
            B /= i;
            mxcnt += 1;
        }
    }
    
    if (A > 1) {
        ++mxcnt;
    }
    if (B > 1) {
        ++mxcnt;
    }

    if (k <= mxcnt) {
        if (k > 1) {
            cout << "YES" << '\n';
        }
        else {
            if (a == 1 && b != 1) {
                cout << "YES" << '\n';
            }
            else if (a != 1 && b == 1) {
                cout << "YES" << '\n';
            }
            else if (a == 1 && b == 1) {
                cout << "NO" << '\n';
            }
            else {
                if (a == b) {
                    cout << "NO" << '\n';
                }
                else {
                    if (max(a, b) % min(a, b) == 0) {
                        cout << "YES" << '\n';
                    }
                    else {
                        cout << "NO" << '\n';
                    }
                }
            }
        }
    }
    else {
        cout << "NO" << '\n';
    }
}

